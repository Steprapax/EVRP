
public class Interval {
	public float start;
	public float end;
	
	public Interval() {
		this.start=0;
		this.end=0;
	}

	/**
	 * @return the start
	 */
	public float getStart() {
		return start;
	}

	/**
	 * @param start the start to set
	 */
	public void setStart(float start) {
		this.start = start;
	}

	/**
	 * @return the end
	 */
	public float getEnd() {
		return end;
	}

	/**
	 * @param end the end to set
	 */
	public void setEnd(float end) {
		this.end = end;
	}
}
